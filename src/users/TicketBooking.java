    private void flightNoComboBoxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_flightNoComboBoxItemStateChanged
        // TODO add your handling code here:
        DB_Connectivity db = new DB_Connectivity();
        ResultSet rs = db.DetailsByFlightNo(flightNoComboBox.getSelectedItem().toString());
        try {
            if(rs.next()){
                String source = rs.getString("source");
                String destination = rs.getString("destination");
                String departure = rs.getString("departure");
                String arrival = rs.getString("arrival");
                String day = rs.getString("day");
                // Set the retrieved data in the text fields
                sourceField.setText(source);
                destinationField.setText(destination);
                departureField.setText(departure);
                arrivalField.setText(arrival);
                flightDayField.setText(day);

            }else{
                // If no row is found, clear the text fields
                sourceField.setText("");
                destinationField.setText("");
            }
        } catch (SQLException ex) {
            Logger.getLogger(TicketBooking.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_flightNoComboBoxItemStateChanged

