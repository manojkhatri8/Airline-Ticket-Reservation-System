    private void passengerIDComboBoxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_passengerIDComboBoxItemStateChanged
        // TODO add your handling code here:
        DB_Connectivity db = new DB_Connectivity();
        ResultSet rs = db.DetailsByPassengerId(passengerIDComboBox.getSelectedItem().toString());
        try {
            if(rs.next()){
                String name = rs.getString("name");
                String cnic = rs.getString("cnic");

                // Set the retrieved data in the text fields
                nameTextField.setText(name);
                cnicTextField.setText(cnic);

            }else{
                // If no row is found, clear the text fields
                nameTextField.setText("");
                cnicTextField.setText("");
            }
        } catch (SQLException ex) {
            Logger.getLogger(TicketBooking.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_passengerIDComboBoxItemStateChanged

